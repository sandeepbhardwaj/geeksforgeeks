	public int findCount(final List<Integer> A, int B) {
		int start = searchIndex(A, B, 0, A.size() - 1, true);
		int end = searchIndex(A, B, 0, A.size() - 1, false);

		if (start == -1)
			return 0;

		int count = end - start + 1;
		return count;
	}

	private int searchIndex(List<Integer> list, int B, int low, int high, boolean startIndex) {
		int result = -1;
		while (low <= high) {
			int mid = (low + high) / 2;

			if (list.get(mid) == B) {
				result = mid;
				if (startIndex)
					high = mid - 1;
				else
					low = mid + 1;
			} else if (list.get(mid) > B) {
				high = mid - 1;
			} else {
				low = mid + 1;
			}
		}
		return result;
	}

